function areaOfCircle(r){
    let areaOfCircle = Math. PI * r*r
    console.log(`Area of circle is ${areaOfCircle}`)
}
areaOfCircle(5)

function areaOfSquare(s){
    let areaOfSquare = 2*s
    console.log(`Area of square is ${areaOfSquare}`)
}
areaOfSquare(10)

function perimeterOfSuqare(s){
    let perimeterOfSuqare = 4*s
    console.log(`perimeter of square is ${perimeterOfSuqare}`)
}
perimeterOfSuqare(6)


function areaOfTriangle(b,h){
    let areaOfTriangle = 0.5*b*h
    console.log(`Area of triangle is ${areaOfTriangle}`)
}
areaOfTriangle(9,11)